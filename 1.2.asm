STACK	SEGMENT	PARA STACK
STACK_AREA      DW      100h DUP(?)
STACK_TOP       EQU     $-STACK_AREA
STACK	ENDS

DATA	SEGMENT	PARA
X 		DW		1606H 
Y		DW		1175H
RESULT_H	DW 		0
RESULT_L 	DW 		0
DATA	ENDS

CODE	SEGMENT	PARA
        ASSUME  CS:CODE,DS:DATA
        ASSUME  SS:STACK

MAIN	PROC	FAR
		MOV 	AX,DATA
		MOV 	DS,AX
		MOV 	AX,STACK
		MOV 	SS,AX
		MOV     SP,STACK_TOP

		MOV		DX,X
		MOV		AX,Y
		MOV		BX,10000
		DIV 	BX
		MOV		RESULT_H,AX
		MOV		RESULT_L,DX
		
		MOV 	CX,0
		MOV 	AX,RESULT_L
		MOV 	BX,10
LP1:	XOR 	DX,DX
		DIV 	BX
		PUSH 	DX
		INC		CX
		CMP 	AX,0
		JNZ 	LP1

		MOV 	AX,RESULT_H
		MOV 	BX,10
LP2:	XOR 	DX,DX
		DIV 	BX
		PUSH 	DX
		INC		CX
		CMP 	AX,0
		JNZ 	LP2	
LP3:
		POP		DX
		OR		DL,30H
		MOV		AH,2
		INT		21H
		LOOP	LP3

EXIT:	MOV		AX,4C00H
		INT		21H
MAIN	ENDP
CODE	ENDS
		END 	MAIN
