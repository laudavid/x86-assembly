DATA	SEGMENT	PARA
N 		DW	7
DATA 	ENDS


CODE 	SEGMENT PARA
		ASSUME	CS:CODE,DS:DATA,SS:STACK

CALCULATE PROC NEAR
		PUSH	BP
		MOV 	BP,SP
		PUSH 	DX
		PUSH 	BX

		MOV 	BX,[BP+6]
		CMP 	BX,0
		JNZ 	CAL1
		MOV 	AX,1
		JMP 	SHORT CAL2
CAL1:
		DEC 	BX
		PUSH 	BX
		PUSH 	0
		CALL 	CALCULATE
		POP 	AX
		POP		BX
		MOV 	BX,[BP+6]
		MUL		BX
CAL2:
		MOV 	[BP+4],AX

		POP	 	BX
		POP 	DX
		POP		BP
		RET
CALCULATE ENDP


DISP_VALUE PROC
		PUSH 	DX
		PUSH 	CX
		PUSH	BX
		PUSH 	AX

		MOV 	CX,5
		MOV 	BX,10
DLP1:
		XOR 	DX,DX
		DIV 	BX
		PUSH 	DX
		LOOP 	DLP1

		MOV 	BX,0
		MOV 	CX,5
DLP2:
		POP 	DX
		CMP 	DL,0
		JNZ 	DLP2_1
		CMP 	BX,0
		JZ 		DLP2_2
DLP2_1:
		MOV 	BX,1
		OR 		DL,30H
		MOV 	AH,2
		INT  	21H
DLP2_2:
		LOOP 	DLP2

		POP 	AX
		POP 	BX
		POP 	CX
		POP 	DX
		RET
DISP_VALUE ENDP


MAIN	PROC 	FAR
		MOV 	AX,DATA
		MOV 	DS,AX

		MOV 	AX,N
		PUSH 	AX
		PUSH 	0
		CALL 	CALCULATE
		POP 	AX
		CALL 	DISP_VALUE	

		MOV 	AX,4C00H
		INT 	21H
MAIN 	ENDP

CODE 	ENDS
		END 	MAIN