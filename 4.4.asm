STACK   SEGMENT PARA    STACK
STACK_AREA  DW              100h DUP(?)
STACK_TOP   EQU             $-STACK_AREA
STACK   ENDS


DATA    SEGMENT PARA
NUM     DD  12345678H
NUM1	DD	90ABCDEFH 
DATA    ENDS

CODE    SEGMENT PARA
		ASSUME  CS:CODE,DS:DATA,SS:STACK

PUTHEX  PROC
		PUSH	DX
		PUSH	AX

		CMP     DL,9
		JA      ABOVE
		OR      DL,30H
		JMP		PUT_RET
ABOVE:
		SUB		DL,9
		OR      DL,40H

PUT_RET:
		MOV     AH,2
		INT     21H

		POP		AX
		POP		DX
		RET
PUTHEX  ENDP


PUTBYTE  PROC
		PUSH	DX
		PUSH	AX
		PUSH	CX

		MOV     DL,[DI]
		MOV		CL,4
		SHR		DL,CL
		CALL	PUTHEX

		MOV     DL,[DI]
		AND		DL,0FH
		CALL	PUTHEX

		POP		CX
		POP		AX
		POP		DX
		RET
PUTBYTE  ENDP


MAIN	PROC 	FAR
		MOV 	AX,DATA
		MOV 	DS,AX
		MOV 	ES,AX

		MOV     DI,OFFSET NUM
		ADD     DI,7
		MOV     CX,8
LP1:
		CALL    PUTBYTE
		DEC     DI
		LOOP    LP1

		MOV 	AX,4C00H
		INT 	21H
MAIN 	ENDP

CODE 	ENDS
		END 	MAIN
